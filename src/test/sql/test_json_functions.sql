select '{"boo":42}' = json_get_object('{"foo":{"boo":42}, "bar": true}', 'foo')
    and 'qq' = json_get_text('{"foo":"qq", "bar": true}', 'foo')
    and true = json_get_boolean('{"foo":"qq", "bar": true}', 'bar')
    and 42 = json_get_int('{"baz": 42, "boo": 42.424242}', 'baz')
    and 42 = json_get_bigint('{"baz": 42, "boo": 42.424242}', 'baz')
    and 42.424242 = json_get_numeric('{"baz": 42, "boo": 42.424242}', 'boo')
    and cast('2009-12-01 01:23:45' as timestamp) = json_get_timestamp('{"foo":"qq", "bar": "2009-12-01 01:23:45"}', 'bar')

    and array['{"foo":42}','{"bar":[]}'] = json_get_object_array('{"boo": [{"foo":42}, {"bar":[]}]}', 'boo')
    and array['["foo","bar"]','[]'] = json_get_multi_array('{"boo": [["foo", "bar"], []]}', 'boo')
    and array['foo','bar'] = json_get_text_array('{"boo": ["foo", "bar"]}', 'boo')
    and array[true,false] = json_get_boolean_array('{"boo": [true, false]}', 'boo')
    and array[42,43] = json_get_int_array('{"boo": [42, 43]}', 'boo')
    and array[cast(42 as bigint), cast(43 as bigint)] = json_get_bigint_array('{"boo": [42, 43]}', 'boo')
    and array[42.4242, 43.4343] = json_get_numeric_array('{"boo": [42.4242, 43.4343]}', 'boo')
    and array[cast('2009-12-01 01:23:45' as timestamp)] = json_get_timestamp_array('{"boo": ["2009-12-01 01:23:45"]}', 'boo')

    and array['{"foo":42}','{"bar":[]}'] = json_array_to_object_array('[{"foo":42}, {"bar":[]}]')
    and array['["foo","bar"]','[]'] = json_array_to_multi_array('[["foo", "bar"], []]')
    and array['foo','bar'] = json_array_to_text_array('["foo", "bar"]')
    and array[true,false] = json_array_to_boolean_array('[true, false]')
    and array[42,43] = json_array_to_int_array('[42, 43]')
    and array[cast(42 as bigint), cast(43 as bigint)] = json_array_to_bigint_array('[42, 43]')
    and array[42.4242, 43.4343] = json_array_to_numeric_array('[42.4242, 43.4343]')
    and array[cast('2009-12-01 01:23:45' as timestamp)] = json_array_to_timestamp_array('["2009-12-01 01:23:45"]')
